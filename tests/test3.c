#include "sha256.h"
#include <stdio.h>
#include <assert.h>

int compare(byte h1[32], byte h2[32])
{
    for(int i = 0; i< 32; i++)
        if(h1[i] != h2[i]) return 0;
    return 1;
}

int main(int argc, char const *argv[])
{
    const char* shortMessage = "abc";
    const char* longMessage = "abcdbcdecdefdefgefghfghighijhijkijkljklmklmnlmnomnopnopq";

    byte expected1[] = {
        0xba, 0x78, 0x16, 0xbf, 0x8f, 0x01, 0xcf, 0xea, 
        0x41, 0x41, 0x40, 0xde, 0x5d, 0xae, 0x22, 0x23, 
        0xb0, 0x03, 0x61, 0xa3, 0x96, 0x17, 0x7a, 0x9c, 
        0xb4, 0x10, 0xff, 0x61, 0xf2, 0x00, 0x15, 0xad
    };
    byte expected2[] = {
        0x24, 0x8d, 0x6a, 0x61, 0xd2, 0x06, 0x38, 0xb8, 
        0xe5, 0xc0, 0x26, 0x93, 0x0c, 0x3e, 0x60, 0x39, 
        0xa3, 0x3c, 0xe4, 0x59, 0x64, 0xff, 0x21, 0x67, 
        0xf6, 0xec, 0xed, 0xd4, 0x19, 0xdb, 0x06, 0xc1
    };
    byte actual[32];

    sha256_hash(shortMessage, 3, actual);
    assert(compare(actual, expected1));

    sha256_hash(longMessage, strlen(longMessage), actual);
    assert(compare(actual, expected2));

    printf("Success\n");
    return 0;
}
